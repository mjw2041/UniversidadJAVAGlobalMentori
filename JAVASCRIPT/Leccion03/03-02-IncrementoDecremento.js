 let a = 3, b = 2;
 let z = a + b;

 // Incremento 
 // PreIncremento ( ++ antes de la variable )
z = ++a;
console.log("Valor de la varible a " +  a) ;
console.log("Valor de la varible z " +  z) ;

 // PostIncremento ( ++ despues de la variable )

 z = b++;
console.log("Valor de la varible b " +  b) ;
console.log("Valor de la varible z " +  z) ;
 

 // Decremento
 // PreDecremento ( -- antes de la variable )
 z = --a;
